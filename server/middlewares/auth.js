const jwt = require("jsonwebtoken");
require("dotenv").config();
const User = require("../models/User");

//authorisation
// This middleware is the authentication middleware. Its job is to check whether the user has a valid JWT token before allowing access to a protected route.
exports.auth = async (req, res, next) => {
    try{
        //extract token
        const token = req.cookies.token 
                        || req.body.token 
                        || req.header("Authorization").replace("Bearer ", "");

        //if token missing, then return response
        if(!token) {
            return res.status(401).json({
                success:false,
                message:'Token is missing',
            });
        }

        //verify the token(use secret key to verify the token)
        try{
            const decode =  jwt.verify(token, process.env.JWT_SECRET); 
            console.log(decode);  // shows the user details stored in the jwt token
            req.user = decode;   // adding user details to request
        }
        catch(err) {
            //verification - issue
            return res.status(401).json({
                success:false,
                message:'token is invalid',
            });
        }
        next();   // move to the next middleware
    }
    catch(error) {  
        return res.status(401).json({
            success:false,
            message:'Something went wrong while validating the token',
        });
    }
}

